package ss1_Introduction_to_Java.Thuc_hanh;

public class KhaiBaoBien {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";
        System.out.printf("Biến i kiểu số nguyên, có giá trị là " + i);
        System.out.printf("Biến f kiểu số thực, có giá trị là " + f);
        System.out.printf("Biến d kiểu số thực lớn, có giá trị là " + d);
        System.out.printf("Biến b kiểu logic, có giá trị là " + b);
        System.out.printf("Biến c kiểu ký tự, có giá trị là " + c);
        System.out.printf("Biến s kiểu chuỗi, có giá trị là " + s);
    }
}
